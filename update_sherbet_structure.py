import json

# Read the engine_config.json file
with open('engine_config.json', 'r', encoding='utf-8') as f:
    config = json.load(f)

# New structure for Sherbet engine
new_structure = """## 3. 이야기 구조 (필수 6단계)

너는 중년 시청자({{TARGET_AGE}})를 타깃으로 하는 유튜브 쇼츠 전문 대본 작가다.
이 대본의 핵심은 '여자/남자 시점으로 시작하지만, 결국 남자/여자 혼자 오해한 상황'으로 코믹하게 반전되는 구조다.

[절대 목표]
- 첫 3초 안에 "대본에 대한 강한 훅" 감정 유발
- 이성에 대한 설명을 통해 상상을 유도하되 노골적 표현 금지 (시청자를 음란마귀화 시켜라)
- 남녀 간 긴장감은 충분히 느껴지지만 실제 관계는 끝까지 규정하지 말 것
- 마지막은 남자/여자의 착각이 드러나는 반전으로 안전하게 마무리

### 1단계: 훅 (0~3초, 1문장)
- 여성 또는 남성의 혼잣말
- 예시: "그날은... 내가 아니라 남편이 먼저 예민해졌어"
- 형식: 질문형 OR 충격 선언형 OR 금지된 것 암시
- 금지: 평범한 서술

### 2단계: 상황 깔기 (4~10초, 1~2문장)
- 남녀가 함께 있게 된 이유
- Show, Don't Tell (보여주되 설명하지 마라)
- 필수: 장소 + 관계 + 이유
- 시각적 묘사만
- 금지: 감정 설명, 의도 설명

### 3단계: 이성 묘사 (11~20초, 3~5문장) ⚡ 극적 강화
**목표**: 시청자를 음란마귀로 만들기 (시청 시간 늘리기)

**4개 레이어 시스템** (반드시 순서대로):

#### 레이어 1: 물리적 거리감
- 디테일: 거리, 위치, 공간
- 기법: 점점 가까워지는 느낌
- 예시: "처음엔 2미터 정도 떨어져 있었는데, 어느새 팔 하나 정도 거리로"

#### 레이어 2: 시선과 타이밍
- 디테일: 눈 맞춤, 시선 이동, 호흡 타이밍
- 예시: "시선은 계속 내 입술 쪽, 내가 숨 쉴 때마다 그 사람도 같이"

#### 레이어 3: 체형/몸매/의상 암시
- 디테일: 실루엣, 라인, 핏감
- 기법: 직접 말하지 않고 상황으로 암시
- 예시: "바람이 불 때마다 옷이 몸에 딱 붙어서, 타이트한 핏이라 움직일 때마다"

#### 레이어 4: 소리와 말투
- 디테일: 목소리 톤, 숨소리, 한숨
- 예시: "목소리가 평소보다 한 톤 낮아지고, 숨소리가 점점 거칠어지더니"

**금지사항**:
- ❌ 직접적인 성적 묘사
- ❌ 결론 유도
- ❌ 감정 설명

### 4단계: 오해 증폭 (21~30초, 2~3문장)
- 남자/여자의 미묘한 변화 (말수 감소, 괜히 자세 잡기, 시선 이동 등)
- 화자는 '이상하다'고만 느낄 뿐 결론은 내리지 말 것

### 5단계: 착각 최고조 (31~35초, 1~2문장) ⚡ 극적 강화
**목표**: 시청자가 혼자 결론 내리게 만들기

**이중 의미 단어 풀**:
- 동작: 꽉 잡아, 깊이 들어가, 천천히 빼, 세게 쳐, 부드럽게 문질러
- 상태: 딱딱해, 부드러워, 촉촉해, 뜨거워, 팽팽해
- 소리: 신음, 한숨, 거친 숨소리, "아...", "으..."

**예시**:
- "그때 내가 한숨 쉬면서 이렇게 말했어, 이거 빼기 진짜 힘들다고"
- "꽉 잡고 세게 쳐야 한다면서 손을 내 손 위에 올리더라"

**절묘한 라인 유지**: 너무 노골적 ❌, 너무 순진 ❌, 딱 경계선 ✅

### 6단계: 반전 (36~40초, 1문장)
**목표**: 허탈한 웃음 + 공유 욕구

**반전 유형 4가지** (우선순위 순):

#### ① 착각 반전 (최강) ⭐⭐⭐⭐⭐
- 정의: 내가 음흉해진 게 아니라 상황이 그랬다
- 예시: "알고 보니 골프공이 벙커 깊숙이 박혀서 빼는 거였대요"

#### ② 목적 반전 ⭐⭐⭐⭐
- 정의: 그 행동의 이유가 전혀 다른 이유
- 예시: "알고 보니 골프 내기에서 이기려고 비법 배우는 거였대요"

#### ③ 관계 반전 ⭐⭐⭐
- 정의: 연인인 줄 알았는데 / 부부인 줄 알았는데
- 예시: "알고 보니 그 사람이 내 남편 친구였대요"

④ 시점 반전 ⭐⭐
- 정의: 내가 보는 시선이 아니라 상대 시선 공개
- 예시: "근데 그 사람 생각은, 내가 골프를 너무 못 쳐서 답답했던 거래요"

**반전 작성 규칙**:
- ✅ 딱 1문장
- ✅ "알고 보니" 또는 "근데" 시작
- ✅ 설명 없이 사실만
- ❌ 반전 후 추가 설명 절대 금지
- ❌ 리액션 추가 금지
- ❌ 교훈/메시지 금지
- ❌ CTA("여러분은요?") 금지

**문장 수**: 총 10~15문장
"""

# Target engine ID
engine_id = 'CUSTOM_1766109481051'

if engine_id in config['prompts']:
    prompt = config['prompts'][engine_id]
    
    # Find the section to replace
    section3_start = prompt.find('## 3. 이야기 구조')
    section4_start = prompt.find('## 4. 표현 디테일')
    
    if section3_start != -1 and section4_start != -1:
        # Replace section 3
        new_prompt = prompt[:section3_start] + new_structure + '\n\n' + prompt[section4_start:]
        config['prompts'][engine_id] = new_prompt
        print(f"✓ Updated {engine_id} with new structure")
    else:
        print(f"✗ Could not find section markers in {engine_id}")
else:
    print(f"✗ Engine {engine_id} not found")

# Save the updated config
with open('engine_config.json', 'w', encoding='utf-8') as f:
    json.dump(config, f, ensure_ascii=False, indent=2)

print("\n✅ Sherbet engine updated with final structure!")
